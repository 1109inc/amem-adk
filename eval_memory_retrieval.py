import asyncio

from google.genai import types

from mentor_agent.amem_memory_service import AMemMemoryService


APP_NAME = "amem_eval"
USER_ID = "eval_user"


class FakeEvent:
    def __init__(self, author: str, text: str):
        self.author = author
        self.id = None
        self.content = types.Content(
            role="user" if author == "user" else "model",
            parts=[types.Part(text=text)],
        )


class FakeSession:
    def __init__(self, app_name: str, user_id: str, events: list[FakeEvent]):
        self.app_name = app_name
        self.user_id = user_id
        self.events = events


def extract_content_from_memory_text(text: str) -> str:
    for line in text.splitlines():
        if line.startswith("Content:"):
            return line.replace("Content:", "").strip()

    return text.splitlines()[0] if text.splitlines() else ""


def get_compact_contents(response) -> list[str]:
    contents = []

    for memory in response.memories:
        text = memory.content.parts[0].text
        content = extract_content_from_memory_text(text)

        if len(content) > 160:
            content = content[:157] + "..."

        contents.append(content)

    return contents


def response_contains_terms(response, terms: list[str]) -> bool:
    combined_text = " ".join(
        memory.content.parts[0].text.lower()
        for memory in response.memories
    )

    return all(term.lower() in combined_text for term in terms)


def print_compact_response(title: str, response) -> None:
    print("\n" + title)

    if not response.memories:
        print("  No memories returned.")
        return

    for index, content in enumerate(get_compact_contents(response), start=1):
        print(f"  {index}. {content}")


def write_eval_results_markdown(
    *,
    direct_vector_hits: int,
    direct_amem_hits: int,
    direct_total: int,
    multi_vector_hits: int,
    multi_amem_hits: int,
    multi_total: int,
    query_results: list[dict],
) -> None:
    overall_vector_hits = direct_vector_hits + multi_vector_hits
    overall_amem_hits = direct_amem_hits + multi_amem_hits
    overall_total = direct_total + multi_total

    lines = [
        "# AMem Retrieval Evaluation Results",
        "",
        "This file is generated by `eval_memory_retrieval.py`.",
        "",
        "## Summary",
        "",
        "| Evaluation Type | Vector-only | A-Mem |",
        "|---|---:|---:|",
        f"| Direct retrieval | {direct_vector_hits}/{direct_total} | {direct_amem_hits}/{direct_total} |",
        f"| Multi-hop retrieval | {multi_vector_hits}/{multi_total} | {multi_amem_hits}/{multi_total} |",
        f"| Overall retrieval | {overall_vector_hits}/{overall_total} | {overall_amem_hits}/{overall_total} |",
        "",
        "## Query Results",
        "",
    ]

    for result in query_results:
        lines.extend(
            [
                f"### {result['group']}: {result['query']}",
                "",
                f"- Expected terms: `{result['expected_terms']}`",
                f"- Vector-only hit: `{result['vector_hit']}`",
                f"- A-Mem hit: `{result['amem_hit']}`",
                "",
                "**Vector-only results:**",
                "",
            ]
        )

        for index, content in enumerate(result["vector_results"], start=1):
            lines.append(f"{index}. {content}")

        lines.extend(["", "**A-Mem results:**", ""])

        for index, content in enumerate(result["amem_results"], start=1):
            lines.append(f"{index}. {content}")

        lines.append("")

    with open("eval_results.md", "w", encoding="utf-8") as file:
        file.write("\n".join(lines))


async def run_query_group(
    memory_service: AMemMemoryService,
    title: str,
    queries: list[dict],
    query_results: list[dict],
) -> tuple[int, int, int]:
    print("\n" + "#" * 100)
    print(title)
    print("#" * 100)

    vector_hits = 0
    amem_hits = 0

    for item in queries:
        query = item["query"]

        vector_response = await memory_service.search_memory_vector_only(
            app_name=APP_NAME,
            user_id=USER_ID,
            query=query,
            top_k=3,
        )

        amem_response = await memory_service.search_memory(
            app_name=APP_NAME,
            user_id=USER_ID,
            query=query,
        )

        vector_hit = response_contains_terms(
            vector_response,
            item["expected_terms"],
        )
        amem_hit = response_contains_terms(
            amem_response,
            item["expected_terms"],
        )

        if vector_hit:
            vector_hits += 1

        if amem_hit:
            amem_hits += 1

        query_results.append(
            {
                "group": title,
                "query": query,
                "expected_terms": item["expected_terms"],
                "vector_hit": vector_hit,
                "amem_hit": amem_hit,
                "vector_results": get_compact_contents(vector_response),
                "amem_results": get_compact_contents(amem_response),
            }
        )

        print("\n\n" + "#" * 100)
        print(f"QUERY: {query}")
        print(f"EXPECTED TERMS: {item['expected_terms']}")
        print(f"\nVECTOR HIT: {vector_hit}")
        print(f"A-MEM HIT: {amem_hit}")
        print("#" * 100)

        print_compact_response("VECTOR-ONLY BASELINE", vector_response)
        print_compact_response("A-MEM GRAPH RETRIEVAL", amem_response)

    total = len(queries)

    print("\n" + "=" * 80)
    print(f"{title} SUMMARY")
    print("=" * 80)
    print(f"Vector-only hits: {vector_hits}/{total}")
    print(f"A-Mem hits: {amem_hits}/{total}")

    return vector_hits, amem_hits, total


